public class AssignmentOps {
    public static void main(String[] args) {
        int x;

        x = 10;
        x += 5;
        System.out.println("After x += 5: " + x);

        x = 10;
        x -= 3;
        System.out.println("After x -= 3: " + x);

        x = 10;
        x *= 2;
        System.out.println("After x *= 2: " + x);

        x = 10;
        x /= 4;
        System.out.println("After x /= 4: " + x);

        x = 10;
        x %= 3;
        System.out.println("After x %= 3: " + x);
    }
}
